// Helper to shuffle an array (Fisher-Yates)
export const shuffleMap = (array: string[]) => {
  const newArray = [...array];
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
  }
  return newArray;
};

// Validate a custom map
export const validateMap = (mapStr: string): { valid: boolean; error?: string; map?: string[] } => {
  const mapArray = Array.from(mapStr);
  
  if (mapArray.length !== 65) {
    return { valid: false, error: `Key must be exactly 65 emojis. Currently: ${mapArray.length}` };
  }
  
  const unique = new Set(mapArray);
  if (unique.size !== 65) {
    return { valid: false, error: 'Key must contain unique emojis. Duplicates found.' };
  }
  
  return { valid: true, map: mapArray };
};
